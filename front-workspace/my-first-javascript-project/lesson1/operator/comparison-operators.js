let a = 1;
let b = 1.0;

console.log("typeof a = " + (typeof a));
console.log("a = " + (a));
console.log("typeof b = " + (typeof b));
console.log("b = " + (b));

// equal to
console.log("a==b = " + (a == b));

// equal value and equal type
console.log("a===b = " + (a === b));

// not equal to
console.log("a!=b = " + (a != b));

// not equal value or not equal type
console.log("a!==b = " + (a !== b));

// greater than
console.log("a>b = " + (a > b));

// greater than or equal to
console.log("a>=b = " + (a >= b));

// less than
console.log("a<b = " + (a < b));

// less than or equal to
console.log("a<=b = " + (a <= b));

