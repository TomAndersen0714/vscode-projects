/*
    Operator    Description
    &           AND
    |           OR
    ~           NOT
    ^           XOR
    <<          left shift
    >>          right shift
    >>>         unsigned right shift
*/

console.log("5 & 1 = " + (5 & 1));

console.log("5 | 1 = " + (5 | 1));

console.log("~5 = " + (~5));

console.log("5 ^ 1 = " + (5 ^ 1));

console.log("5 << 1 = " + (5 << 1));

console.log("5 >> 1 = " + (5 >> 1));

console.log("5 >>> 1 = " + (5 >>> 1));